function testingSwitch() {
  const nilai = 10;

  switch (nilai) {
    case 10:
      console.log("Sangat Bagus");
      break;
    case 8:
      console.log("Lumayan");
      break;
    case 6:
      console.log("Cukup");
      break;
    default:
      console.log("Lainnya");
  }
}
testingSwitch();
